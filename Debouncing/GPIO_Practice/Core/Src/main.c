static inline GPIO_PinState Premier_ClickBTN (void){
	return HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_15);
}

static GPIO_PinState StableBTN(void){
	GPIO_PinState firstClick = Premier_ClickBTN(); // premier appuis
	uint32_t timeNow = HAL_GetTick();
	if (firstClick != btnRawPrev){
		btnRawPrev = firstClick;
		debouncing = 1;
		dernierChangeBTN = timeNow;
	}

	if (debouncing && ((timeNow - dernierChangeBTN) >= TEMPSREBONG)){
		debouncing = 0;
		if (firstClick != btnStable){
			btnStable = firstClick;
		}

	}

	return btnStable;
}
